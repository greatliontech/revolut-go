package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

// emit writes the generated Go source files for spec into outDir.
func emit(spec *Spec, outDir string) error {
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return err
	}

	unionTypes := map[string]bool{}
	for _, t := range spec.Types {
		if t.Kind == KindUnion {
			unionTypes[t.GoName] = true
		}
	}

	// Request-body content types per generated struct. Used to emit
	// encodeForm / encodeMultipart methods next to the struct.
	bodyEncoders := map[string]string{}
	for _, r := range spec.Resources {
		for _, op := range r.Ops {
			if op.RequestType == "" || op.RequestContentType == "" {
				continue
			}
			bodyEncoders[op.RequestType] = op.RequestContentType
		}
	}

	// gen_types.go: every named type.
	typesPath := filepath.Join(outDir, "gen_types.go")
	if err := writeFormatted(typesPath, emitTypes(spec, bodyEncoders)); err != nil {
		return fmt.Errorf("emit types: %w", err)
	}

	// gen_<resource>.go: resource struct + methods.
	for _, r := range spec.Resources {
		name := "gen_" + strings.ToLower(r.GoName) + ".go"
		path := filepath.Join(outDir, name)
		if err := writeFormatted(path, emitResource(spec, r, unionTypes)); err != nil {
			return fmt.Errorf("emit %s: %w", r.GoName, err)
		}
	}

	// gen_client.go: the Client struct and New wiring.
	clientPath := filepath.Join(outDir, "gen_client.go")
	if err := writeFormatted(clientPath, emitClient(spec)); err != nil {
		return fmt.Errorf("emit client: %w", err)
	}

	return nil
}

// emitClient produces gen_client.go: the Client struct with a field per
// resource plus the New constructor wiring them.
func emitClient(spec *Spec) string {
	var buf bytes.Buffer
	buf.WriteString(genHeader)
	fmt.Fprintf(&buf, "package %s\n\n", spec.PackageName)
	buf.WriteString("import \"github.com/greatliontech/revolut-go/internal/transport\"\n\n")
	buf.WriteString("// Client is the generated Revolut API client. Resource fields group\n")
	buf.WriteString("// endpoints by their Revolut API path prefix.\n")
	buf.WriteString("type Client struct {\n")
	buf.WriteString("\ttransport *transport.Transport\n\n")
	for _, r := range spec.Resources {
		fmt.Fprintf(&buf, "\t// %s groups the related endpoints.\n", r.GoName)
		fmt.Fprintf(&buf, "\t%s *%s\n", r.GoName, r.GoName)
	}
	buf.WriteString("}\n\n")
	buf.WriteString("// New wraps an HTTP transport in a Client.\n")
	buf.WriteString("func New(t *transport.Transport) *Client {\n")
	buf.WriteString("\tc := &Client{transport: t}\n")
	for _, r := range spec.Resources {
		fmt.Fprintf(&buf, "\tc.%s = &%s{t: t}\n", r.GoName, r.GoName)
	}
	buf.WriteString("\treturn c\n}\n")
	return buf.String()
}

const genHeader = "// Code generated by revogen. DO NOT EDIT.\n\n"

// emitTypes produces the gen_types.go source.
func emitTypes(spec *Spec, bodyEncoders map[string]string) string {
	var buf bytes.Buffer
	buf.WriteString(genHeader)
	fmt.Fprintf(&buf, "package %s\n\n", spec.PackageName)

	needsJSON := false
	needsTime := false
	needsCore := false
	needsIO := false
	for _, t := range spec.Types {
		if t.Kind == KindAlias && strings.HasPrefix(t.AliasTarget, "core.") {
			needsCore = true
		}
		if t.Kind == KindUnion {
			// Union decoder helpers use encoding/json to probe variants.
			needsJSON = true
		}
		for _, f := range t.Fields {
			if strings.Contains(f.GoType, "json.Number") {
				needsJSON = true
			}
			if strings.Contains(f.GoType, "time.Time") {
				needsTime = true
			}
			if strings.Contains(f.GoType, "core.") {
				needsCore = true
			}
			if strings.Contains(f.GoType, "io.Reader") {
				needsIO = true
			}
		}
	}

	// Index unions by the variant structs they claim, so we can emit a
	// marker method right after each variant's struct declaration.
	unionMarkers := map[string][]string{} // variantGoName -> []unionGoName
	for _, t := range spec.Types {
		if t.Kind != KindUnion {
			continue
		}
		for _, v := range t.UnionVariants {
			unionMarkers[v.GoName] = append(unionMarkers[v.GoName], t.GoName)
		}
	}

	typesByName := map[string]*NamedType{}
	hasUnion := false
	hasMultipart := false
	hasForm := false
	for _, t := range spec.Types {
		typesByName[t.GoName] = t
		if t.Kind == KindUnion {
			hasUnion = true
		}
		switch bodyEncoders[t.GoName] {
		case "multipart/form-data":
			hasMultipart = true
		case "application/x-www-form-urlencoded":
			hasForm = true
		}
	}

	buf.WriteString("import (\n")
	if hasMultipart {
		buf.WriteString("\t\"bytes\"\n")
	}
	if needsJSON {
		buf.WriteString("\t\"encoding/json\"\n")
	}
	if hasForm || hasMultipart {
		buf.WriteString("\t\"fmt\"\n")
	}
	if needsIO || hasMultipart {
		buf.WriteString("\t\"io\"\n")
	}
	if hasMultipart {
		buf.WriteString("\t\"mime/multipart\"\n")
	}
	if hasForm {
		buf.WriteString("\t\"net/url\"\n")
	}
	if hasForm || hasMultipart {
		buf.WriteString("\t\"strconv\"\n")
	}
	if needsTime || hasForm || hasMultipart {
		buf.WriteString("\t\"time\"\n")
	}
	if needsCore {
		if needsJSON || needsTime || needsIO || hasMultipart || hasForm {
			buf.WriteString("\n")
		}
		buf.WriteString("\t\"github.com/greatliontech/revolut-go/internal/core\"\n")
	}
	buf.WriteString(")\n\n")
	if hasUnion {
		buf.WriteString("// hasKey reports whether every required JSON key of a union variant\n")
		buf.WriteString("// is present on the wire. Used by the generated decode<Union> helpers.\n")
		buf.WriteString("func hasKey(m map[string]json.RawMessage, key string) bool {\n")
		buf.WriteString("\t_, ok := m[key]\n")
		buf.WriteString("\treturn ok\n")
		buf.WriteString("}\n\n")
	}
	if hasForm || hasMultipart {
		buf.WriteString("// formValueString stringifies a scalar field for form/multipart\n")
		buf.WriteString("// encoding. Unsupported shapes fall through to fmt.Sprint.\n")
		buf.WriteString("func formValueString(v any) string {\n")
		buf.WriteString("\tswitch x := v.(type) {\n")
		buf.WriteString("\tcase string:\n\t\treturn x\n")
		buf.WriteString("\tcase bool:\n\t\tif x { return \"true\" }\n\t\treturn \"false\"\n")
		buf.WriteString("\tcase int:\n\t\treturn strconv.FormatInt(int64(x), 10)\n")
		buf.WriteString("\tcase int32:\n\t\treturn strconv.FormatInt(int64(x), 10)\n")
		buf.WriteString("\tcase int64:\n\t\treturn strconv.FormatInt(x, 10)\n")
		buf.WriteString("\tcase float32:\n\t\treturn strconv.FormatFloat(float64(x), 'g', -1, 32)\n")
		buf.WriteString("\tcase float64:\n\t\treturn strconv.FormatFloat(x, 'g', -1, 64)\n")
		buf.WriteString("\tcase time.Time:\n\t\treturn x.UTC().Format(time.RFC3339Nano)\n")
		buf.WriteString("\tcase fmt.Stringer:\n\t\treturn x.String()\n")
		buf.WriteString("\t}\n")
		buf.WriteString("\treturn fmt.Sprintf(\"%v\", v)\n")
		buf.WriteString("}\n\n")
	}

	for _, t := range spec.Types {
		writeType(&buf, t, typesByName)
		// Emit marker methods on variant structs so they implement
		// any unions they belong to. Placed next to the struct so
		// it's discoverable via godoc.
		if t.Kind == KindStruct {
			for _, union := range unionMarkers[t.GoName] {
				fmt.Fprintf(&buf, "func (%s) is%s() {}\n\n", t.GoName, union)
			}
			switch bodyEncoders[t.GoName] {
			case "application/x-www-form-urlencoded":
				writeFormEncoder(&buf, t)
			case "multipart/form-data":
				writeMultipartEncoder(&buf, t)
			}
		}
	}
	return buf.String()
}

// writeFormEncoder emits an encodeForm() helper that converts a
// request-body struct into url.Values suitable for posting as
// application/x-www-form-urlencoded. Scalar fields are coerced to
// strings via fmt.Sprint; zero-valued fields are skipped so servers
// see only explicitly-set keys.
func writeFormEncoder(buf *bytes.Buffer, t *NamedType) {
	fmt.Fprintf(buf, "// encodeForm builds the %s URL-encoded form body.\n", t.GoName)
	fmt.Fprintf(buf, "func (r *%s) encodeForm() url.Values {\n", t.GoName)
	buf.WriteString("\tv := url.Values{}\n")
	buf.WriteString("\tif r == nil {\n\t\treturn v\n\t}\n")
	for _, f := range t.Fields {
		expr := "r." + f.GoName
		// Skip binary reader fields — form-urlencoded cannot carry them.
		if f.GoType == "io.Reader" {
			continue
		}
		cond := unsetCond(f.GoType, expr, nil)
		if cond == "" {
			fmt.Fprintf(buf, "\tv.Set(%q, formValueString(%s))\n", f.JSONName, expr)
			continue
		}
		fmt.Fprintf(buf, "\tif !(%s) {\n\t\tv.Set(%q, formValueString(%s))\n\t}\n", cond, f.JSONName, expr)
	}
	buf.WriteString("\treturn v\n}\n\n")
}

// writeMultipartEncoder emits encodeMultipart() that builds a
// multipart/form-data body. String fields become plain parts;
// io.Reader fields become file parts with the JSON name as the form
// field. The returned content-type includes the chosen boundary.
func writeMultipartEncoder(buf *bytes.Buffer, t *NamedType) {
	fmt.Fprintf(buf, "// encodeMultipart builds the %s multipart/form-data body.\n", t.GoName)
	fmt.Fprintf(buf, "func (r *%s) encodeMultipart() (io.Reader, string, error) {\n", t.GoName)
	buf.WriteString("\tvar body bytes.Buffer\n")
	buf.WriteString("\tw := multipart.NewWriter(&body)\n")
	buf.WriteString("\tif r != nil {\n")
	for _, f := range t.Fields {
		expr := "r." + f.GoName
		if f.GoType == "io.Reader" {
			fmt.Fprintf(buf, "\t\tif %s != nil {\n", expr)
			fmt.Fprintf(buf, "\t\t\tpart, err := w.CreateFormFile(%q, %q)\n", f.JSONName, f.JSONName)
			buf.WriteString("\t\t\tif err != nil { return nil, \"\", err }\n")
			fmt.Fprintf(buf, "\t\t\tif _, err := io.Copy(part, %s); err != nil { return nil, \"\", err }\n", expr)
			buf.WriteString("\t\t}\n")
			continue
		}
		cond := unsetCond(f.GoType, expr, nil)
		if cond == "" {
			fmt.Fprintf(buf, "\t\tif err := w.WriteField(%q, formValueString(%s)); err != nil { return nil, \"\", err }\n", f.JSONName, expr)
			continue
		}
		fmt.Fprintf(buf, "\t\tif !(%s) {\n", cond)
		fmt.Fprintf(buf, "\t\t\tif err := w.WriteField(%q, formValueString(%s)); err != nil { return nil, \"\", err }\n", f.JSONName, expr)
		buf.WriteString("\t\t}\n")
	}
	buf.WriteString("\t}\n")
	buf.WriteString("\tif err := w.Close(); err != nil { return nil, \"\", err }\n")
	buf.WriteString("\treturn &body, w.FormDataContentType(), nil\n}\n\n")
}

func writeType(buf *bytes.Buffer, t *NamedType, typesByName map[string]*NamedType) {
	if t.Doc != "" {
		writeDoc(buf, t.Doc, t.GoName)
	}
	switch t.Kind {
	case KindAlias:
		fmt.Fprintf(buf, "type %s = %s\n\n", t.GoName, t.AliasTarget)
	case KindEnum:
		fmt.Fprintf(buf, "type %s %s\n\n", t.GoName, t.EnumBase)
		if len(t.EnumValues) > 0 {
			buf.WriteString("const (\n")
			for _, v := range t.EnumValues {
				if v.Doc != "" {
					fmt.Fprintf(buf, "\t// %s.\n", v.Doc)
				}
				fmt.Fprintf(buf, "\t%s %s = %q\n", v.GoName, t.GoName, v.Value)
			}
			buf.WriteString(")\n\n")
		}
	case KindStruct:
		fmt.Fprintf(buf, "type %s struct {\n", t.GoName)
		for i, f := range t.Fields {
			if i > 0 {
				buf.WriteString("\n")
			}
			writeStructFieldDoc(buf, f)
			fmt.Fprintf(buf, "\t%s %s `json:\"%s\"`\n", f.GoName, f.GoType, jsonTag(f))
		}
		buf.WriteString("}\n\n")
	case KindUnion:
		// Sealed interface. Variants (emitted elsewhere as structs) add
		// the marker method to satisfy the interface.
		//
		// Revolut's unions carry a `discriminator.mapping` but no
		// `propertyName`, so there is no wire-level tag. Request bodies
		// are marshaled naturally via the concrete variant; response
		// bodies are buffered and decoded via decode<Parent>, which
		// probes variants in mapping order.
		fmt.Fprintf(buf, "// Variants:\n")
		for _, v := range t.UnionVariants {
			fmt.Fprintf(buf, "//   - %s → %s\n", v.Tag, v.GoName)
		}
		fmt.Fprintf(buf, "type %s interface {\n", t.GoName)
		fmt.Fprintf(buf, "\tis%s()\n", t.GoName)
		buf.WriteString("}\n\n")
		writeUnionDecoder(buf, t, typesByName)
	}
}

// writeUnionDecoder emits a decode<Parent>(data []byte) (<Parent>, error)
// helper. Since Revolut's unions lack a wire discriminator, the helper
// probes variants in mapping order by checking that every required JSON
// field of the variant is present in the payload. First match wins; if
// nothing matches (e.g. variants share required fields), the first
// mapped variant is decoded.
func writeUnionDecoder(buf *bytes.Buffer, t *NamedType, typesByName map[string]*NamedType) {
	fmt.Fprintf(buf, "// decode%s parses a JSON payload into one of the union variants.\n", t.GoName)
	buf.WriteString("// Variants are tried in discriminator-mapping order. If none uniquely\n")
	buf.WriteString("// match by required-field presence the first mapped variant is used.\n")
	fmt.Fprintf(buf, "func decode%s(data []byte) (%s, error) {\n", t.GoName, t.GoName)
	buf.WriteString("\tvar probe map[string]json.RawMessage\n")
	buf.WriteString("\tif err := json.Unmarshal(data, &probe); err != nil {\n")
	buf.WriteString("\t\treturn nil, err\n")
	buf.WriteString("\t}\n")
	for _, v := range t.UnionVariants {
		reqs := variantRequiredJSONKeys(typesByName, v.GoName)
		if len(reqs) == 0 {
			continue
		}
		cond := "true"
		parts := make([]string, 0, len(reqs))
		for _, k := range reqs {
			parts = append(parts, fmt.Sprintf("hasKey(probe, %q)", k))
		}
		cond = strings.Join(parts, " && ")
		fmt.Fprintf(buf, "\tif %s {\n", cond)
		fmt.Fprintf(buf, "\t\tvar out %s\n", v.GoName)
		buf.WriteString("\t\tif err := json.Unmarshal(data, &out); err != nil {\n")
		buf.WriteString("\t\t\treturn nil, err\n")
		buf.WriteString("\t\t}\n")
		buf.WriteString("\t\treturn out, nil\n")
		buf.WriteString("\t}\n")
	}
	first := t.UnionVariants[0]
	fmt.Fprintf(buf, "\tvar out %s\n", first.GoName)
	buf.WriteString("\tif err := json.Unmarshal(data, &out); err != nil {\n")
	buf.WriteString("\t\treturn nil, err\n")
	buf.WriteString("\t}\n")
	buf.WriteString("\treturn out, nil\n")
	buf.WriteString("}\n\n")
}

// variantRequiredJSONKeys returns the JSON names of required fields on a
// variant struct type. Used by the union probe decoder.
func variantRequiredJSONKeys(typesByName map[string]*NamedType, name string) []string {
	t, ok := typesByName[name]
	if !ok || t.Kind != KindStruct {
		return nil
	}
	var out []string
	for _, f := range t.Fields {
		if f.Required {
			out = append(out, f.JSONName)
		}
	}
	return out
}

// emitResource produces gen_<resource>.go.
func emitResource(spec *Spec, r *Resource, unionTypes map[string]bool) string {
	var buf bytes.Buffer
	buf.WriteString(genHeader)
	fmt.Fprintf(&buf, "package %s\n\n", spec.PackageName)

	// Imports — compute from what the methods reference.
	imports := resourceImports(r, unionTypes)

	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	if imports.errors {
		buf.WriteString("\t\"errors\"\n")
	}
	buf.WriteString("\t\"net/http\"\n")
	if imports.url {
		buf.WriteString("\t\"net/url\"\n")
	}
	if imports.strconv {
		buf.WriteString("\t\"strconv\"\n")
	}
	if imports.time {
		buf.WriteString("\t\"time\"\n")
	}
	if imports.json {
		buf.WriteString("\t\"encoding/json\"\n")
	}
	if imports.iter {
		buf.WriteString("\t\"iter\"\n")
	}
	buf.WriteString("\n\t\"github.com/greatliontech/revolut-go/internal/transport\"\n")
	buf.WriteString(")\n\n")

	fmt.Fprintf(&buf, "// %s groups the %s endpoints.\n", r.GoName, r.GoName)
	fmt.Fprintf(&buf, "type %s struct {\n", r.GoName)
	buf.WriteString("\tt *transport.Transport\n")
	buf.WriteString("}\n\n")

	for _, op := range r.Ops {
		if op.ParamsStruct != nil {
			writeType(&buf, op.ParamsStruct, nil)
			writeParamsEncode(&buf, op)
		}
		writeMethod(&buf, r, op, unionTypes, spec.ErrPrefix)
		if op.Pagination != nil {
			writePaginationMethod(&buf, r, op, spec.ErrPrefix)
		}
	}
	return buf.String()
}

// writePaginationMethod emits a <GoMethod>All iterator that walks every
// page of a paginated endpoint and yields one item at a time.
func writePaginationMethod(buf *bytes.Buffer, r *Resource, op *Operation, errPrefix string) {
	p := op.Pagination
	name := op.GoMethod + "All"

	// godoc.
	fmt.Fprintf(buf, "// %s iterates every page of %s, yielding one %s per\n",
		name, op.GoMethod, p.ItemType)
	buf.WriteString("// step. The iterator terminates when the underlying endpoint signals\n")
	switch p.Shape {
	case PaginationCursor:
		buf.WriteString("// no further cursor. Break out of the loop to stop early.\n")
	case PaginationTimeWindow:
		buf.WriteString("// an empty page. Break out of the loop to stop early.\n")
	}
	buf.WriteString("//\n")
	buf.WriteString("// Pass nil for opts to accept the server's defaults on the first page.\n")
	buf.WriteString("// A non-nil opts is copied internally so the caller's struct is not\n")
	buf.WriteString("// mutated as pages advance.\n")

	// Signature.
	params := []string{"ctx context.Context"}
	for _, pp := range op.PathParams {
		params = append(params, pp.GoName+" "+pp.GoType)
	}
	if op.ParamsType != "" {
		params = append(params, "opts *"+op.ParamsType)
	}
	fmt.Fprintf(buf, "func (s *%s) %s(%s) iter.Seq2[%s, error] {\n",
		r.GoName, name, strings.Join(params, ", "), p.ItemType)

	fmt.Fprintf(buf, "\treturn func(yield func(%s, error) bool) {\n", p.ItemType)

	// Copy opts so we can mutate the cursor fields in place.
	fmt.Fprintf(buf, "\t\tvar p %s\n", op.ParamsType)
	buf.WriteString("\t\tif opts != nil {\n\t\t\tp = *opts\n\t\t}\n")

	// Path-param validation (same checks as the single-page method).
	for _, pp := range op.PathParams {
		fmt.Fprintf(buf, "\t\tif %s == \"\" {\n", pp.GoName)
		fmt.Fprintf(buf, "\t\t\tvar zero %s\n", p.ItemType)
		fmt.Fprintf(buf, "\t\t\tyield(zero, errors.New(%q))\n", errPrefix+": "+pp.Name+" is required")
		buf.WriteString("\t\t\treturn\n")
		buf.WriteString("\t\t}\n")
	}

	// Loop.
	buf.WriteString("\t\tfor {\n")
	// s.Method(ctx[, pathParams], &p)
	call := "s." + op.GoMethod + "(ctx"
	for _, pp := range op.PathParams {
		call += ", " + pp.GoName
	}
	call += ", &p)"

	fmt.Fprintf(buf, "\t\t\tresp, err := %s\n", call)
	buf.WriteString("\t\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\t\tvar zero %s\n", p.ItemType)
	buf.WriteString("\t\t\t\tyield(zero, err)\n")
	buf.WriteString("\t\t\t\treturn\n")
	buf.WriteString("\t\t\t}\n")

	switch p.Shape {
	case PaginationCursor:
		fmt.Fprintf(buf, "\t\t\tfor _, item := range resp.%s {\n", p.ItemsField)
		buf.WriteString("\t\t\t\tif !yield(item, nil) {\n\t\t\t\t\treturn\n\t\t\t\t}\n")
		buf.WriteString("\t\t\t}\n")
		// NextPageToken may be a plain string or a pointer/alias. Normalise
		// both to a string value for the exhaustion check and the param
		// assignment.
		tokenExpr := "resp." + p.NextTokenField
		// Source exhaustion check: treat nil/empty as "no more pages".
		if strings.HasPrefix(p.NextTokenType, "*") {
			fmt.Fprintf(buf, "\t\t\tif %s == nil || *%s == \"\" {\n\t\t\t\treturn\n\t\t\t}\n", tokenExpr, tokenExpr)
			tokenExpr = "*" + tokenExpr
		} else {
			fmt.Fprintf(buf, "\t\t\tif %s == \"\" {\n\t\t\t\treturn\n\t\t\t}\n", tokenExpr)
		}
		// Assign to param, converting if the param type differs (e.g. named
		// string alias vs raw string).
		assign := tokenExpr
		if p.PageTokenType != "" && p.PageTokenType != strings.TrimPrefix(p.NextTokenType, "*") {
			assign = p.PageTokenType + "(" + tokenExpr + ")"
		}
		fmt.Fprintf(buf, "\t\t\tp.%s = %s\n", p.PageTokenParam, assign)
	case PaginationTimeWindow:
		buf.WriteString("\t\t\tif len(resp) == 0 {\n\t\t\t\treturn\n\t\t\t}\n")
		buf.WriteString("\t\t\tfor _, item := range resp {\n")
		buf.WriteString("\t\t\t\tif !yield(item, nil) {\n\t\t\t\t\treturn\n\t\t\t\t}\n")
		buf.WriteString("\t\t\t}\n")
		fmt.Fprintf(buf, "\t\t\tp.%s = resp[len(resp)-1].%s\n", p.AdvanceParam, p.AdvanceFromItem)
	}
	buf.WriteString("\t\t}\n")
	buf.WriteString("\t}\n")
	buf.WriteString("}\n\n")
}

type resourceImportSet struct {
	errors  bool
	url     bool
	strconv bool
	time    bool
	json    bool
	iter    bool
}

func resourceImports(r *Resource, unionTypes map[string]bool) resourceImportSet {
	var s resourceImportSet
	for _, op := range r.Ops {
		if len(op.PathParams) > 0 {
			s.url = true
			s.errors = true
		}
		if len(op.Validate) > 0 {
			s.errors = true
		}
		if op.ParamsStruct != nil {
			s.url = true
			for _, f := range op.ParamsStruct.Fields {
				switch {
				case f.GoType == "int", f.GoType == "int64", f.GoType == "bool":
					s.strconv = true
				case f.GoType == "time.Time":
					s.time = true
				case f.GoType == "json.Number":
					s.json = true
				case strings.HasPrefix(f.GoType, "[]"):
					// Encoded as repeated values; no extra import.
				}
			}
		}
		if op.Pagination != nil {
			s.iter = true
		}
		if unionTypes[op.ResponseType] {
			// Union response → buffered raw decode via json.RawMessage.
			s.json = true
		}
		if op.RequestContentType != "" || op.ResponseContentType != "" {
			// DoRaw path: we post-decode JSON fragments and always
			// construct a transport.RawRequest literal.
			if op.ResponseType != "" && op.ResponseType != "[]byte" {
				s.json = true
			}
		}
	}
	return s
}

// writeParamsEncode emits an `encode` method for a params struct that
// produces a url.Values with only the non-zero fields set, so callers
// pay nothing for params they don't fill in.
func writeParamsEncode(buf *bytes.Buffer, op *Operation) {
	t := op.ParamsStruct
	fmt.Fprintf(buf, "func (p *%s) encode() url.Values {\n", t.GoName)
	buf.WriteString("\tif p == nil {\n\t\treturn nil\n\t}\n")
	buf.WriteString("\tq := url.Values{}\n")
	for i, f := range t.Fields {
		qp := op.QueryParams[i]
		emitQueryField(buf, f, qp.Name)
	}
	buf.WriteString("\treturn q\n}\n\n")
}

// emitQueryField writes the "if field has a value, q.Set(...)" for one
// params-struct field.
func emitQueryField(buf *bytes.Buffer, f *StructField, wireName string) {
	expr := "p." + f.GoName
	if strings.HasPrefix(f.GoType, "[]") {
		// Repeated query parameter: emit one ?name=v per element.
		inner := strings.TrimPrefix(f.GoType, "[]")
		valueExpr := "v"
		switch inner {
		case "string":
			// keep v as-is.
		case "int", "int64":
			valueExpr = "strconv.FormatInt(int64(v), 10)"
		case "bool":
			valueExpr = "strconv.FormatBool(v)"
		case "time.Time":
			valueExpr = "v.UTC().Format(time.RFC3339Nano)"
		default:
			valueExpr = "string(v)"
		}
		fmt.Fprintf(buf, "\tfor _, v := range %s {\n\t\tq.Add(%q, %s)\n\t}\n", expr, wireName, valueExpr)
		return
	}
	switch f.GoType {
	case "string":
		fmt.Fprintf(buf, "\tif %s != \"\" {\n\t\tq.Set(%q, %s)\n\t}\n", expr, wireName, expr)
	case "int", "int64":
		fmt.Fprintf(buf, "\tif %s != 0 {\n\t\tq.Set(%q, strconv.FormatInt(int64(%s), 10))\n\t}\n", expr, wireName, expr)
	case "bool":
		fmt.Fprintf(buf, "\tif %s {\n\t\tq.Set(%q, strconv.FormatBool(%s))\n\t}\n", expr, wireName, expr)
	case "time.Time":
		fmt.Fprintf(buf, "\tif !%s.IsZero() {\n\t\tq.Set(%q, %s.UTC().Format(time.RFC3339Nano))\n\t}\n", expr, wireName, expr)
	case "json.Number":
		fmt.Fprintf(buf, "\tif %s != \"\" {\n\t\tq.Set(%q, string(%s))\n\t}\n", expr, wireName, expr)
	default:
		// Enum types (string-backed) and named aliases.
		fmt.Fprintf(buf, "\tif %s != \"\" {\n\t\tq.Set(%q, string(%s))\n\t}\n", expr, wireName, expr)
	}
}

func writeMethod(buf *bytes.Buffer, r *Resource, op *Operation, unionTypes map[string]bool, errPrefix string) {
	// godoc
	if op.Summary != "" {
		fmt.Fprintf(buf, "// %s %s\n", op.GoMethod, lower1(op.Summary))
	} else {
		fmt.Fprintf(buf, "// %s performs the %s operation.\n", op.GoMethod, op.GoMethod)
	}
	if op.DocURL != "" {
		buf.WriteString("//\n")
		fmt.Fprintf(buf, "// Docs: %s\n", op.DocURL)
	}

	// Signature.
	params := []string{"ctx context.Context"}
	for _, p := range op.PathParams {
		params = append(params, p.GoName+" "+p.GoType)
	}
	if op.RequestType != "" {
		params = append(params, "req "+op.RequestType)
	}
	if op.ParamsType != "" {
		params = append(params, "opts *"+op.ParamsType)
	}

	retType := "error"
	respIsUnion := unionTypes[op.ResponseType]
	switch {
	case op.ResponseType == "":
		// void
	case respIsUnion:
		// Union → return the interface type directly; no pointer.
		retType = op.ResponseType + ", error"
	case strings.HasPrefix(op.ResponseType, "[]"):
		retType = op.ResponseType + ", error"
	default:
		retType = "*" + op.ResponseType + ", error"
	}

	fmt.Fprintf(buf, "func (s *%s) %s(%s) (%s) {\n", r.GoName, op.GoMethod, strings.Join(params, ", "), retType)

	// Path-param validation.
	zero := zeroValueForReturn(op.ResponseType)
	for _, p := range op.PathParams {
		fmt.Fprintf(buf, "\tif %s == \"\" {\n", p.GoName)
		fmt.Fprintf(buf, "\t\treturn %serrors.New(%q)\n", ifZero(zero), errPrefix+": "+p.Name+" is required")
		buf.WriteString("\t}\n")
	}
	// Body-field validation.
	for _, v := range op.Validate {
		fmt.Fprintf(buf, "\tif %s {\n", v.Cond)
		fmt.Fprintf(buf, "\t\treturn %serrors.New(%q)\n", ifZero(zero), v.Message)
		buf.WriteString("\t}\n")
	}

	// HTTP call.
	pathExpr := renderPathExpr(op.PathTemplate, op.PathParams)
	if op.ParamsType != "" {
		fmt.Fprintf(buf, "\tpath := %s\n", pathExpr)
		buf.WriteString("\tif q := opts.encode().Encode(); q != \"\" {\n")
		buf.WriteString("\t\tpath += \"?\" + q\n")
		buf.WriteString("\t}\n")
		pathExpr = "path"
	}
	bodyArg := "nil"
	if op.RequestType != "" {
		bodyArg = "req"
	}
	httpMethod := "http.Method" + strings.ToUpper(op.HTTPMethod[:1]) + strings.ToLower(op.HTTPMethod[1:])

	switch {
	case op.RequestContentType != "" || op.ResponseContentType != "":
		writeRawHTTPCall(buf, op, httpMethod, pathExpr, zero)
	case op.ResponseType == "":
		fmt.Fprintf(buf, "\treturn s.t.Do(ctx, %s, %s, %s, nil)\n", httpMethod, pathExpr, bodyArg)
	case respIsUnion:
		// Buffer the raw body, then probe-decode into a variant.
		buf.WriteString("\tvar raw json.RawMessage\n")
		fmt.Fprintf(buf, "\tif err := s.t.Do(ctx, %s, %s, %s, &raw); err != nil {\n", httpMethod, pathExpr, bodyArg)
		buf.WriteString("\t\treturn nil, err\n")
		buf.WriteString("\t}\n")
		fmt.Fprintf(buf, "\treturn decode%s(raw)\n", op.ResponseType)
	default:
		fmt.Fprintf(buf, "\tvar out %s\n", op.ResponseType)
		fmt.Fprintf(buf, "\tif err := s.t.Do(ctx, %s, %s, %s, &out); err != nil {\n", httpMethod, pathExpr, bodyArg)
		fmt.Fprintf(buf, "\t\treturn %serr\n", ifZero(zero))
		buf.WriteString("\t}\n")
		if strings.HasPrefix(op.ResponseType, "[]") {
			buf.WriteString("\treturn out, nil\n")
		} else {
			buf.WriteString("\treturn &out, nil\n")
		}
	}
	buf.WriteString("}\n\n")
}

// writeRawHTTPCall emits the non-JSON transport call: a single
// transport.DoRaw invocation that handles form-urlencoded, multipart,
// octet-stream, and raw-byte responses. Callers always get back
// ([]byte, error) when the response is non-JSON, or (*T, error) for
// JSON responses paired with non-JSON request bodies.
func writeRawHTTPCall(buf *bytes.Buffer, op *Operation, httpMethod, pathExpr, zero string) {
	// Build the transport.RawRequest literal.
	buf.WriteString("\tr := transport.RawRequest{\n")
	switch op.RequestContentType {
	case "":
		if op.RequestType != "" {
			buf.WriteString("\t\tJSONBody: req,\n")
		}
	case "application/x-www-form-urlencoded":
		buf.WriteString("\t\tFormBody: req.encodeForm(),\n")
	case "multipart/form-data":
		buf.WriteString("\t\tRawBody: nil, RawContentType: \"\",\n")
		// Overwritten below — we emit a helper call because multipart
		// content-type carries a boundary we only know after building
		// the body.
	case "application/octet-stream":
		buf.WriteString("\t\tRawBody: req,\n")
		buf.WriteString("\t\tRawContentType: \"application/octet-stream\",\n")
	}
	if op.ResponseContentType != "" {
		fmt.Fprintf(buf, "\t\tAccept: %q,\n", op.ResponseContentType)
	}
	buf.WriteString("\t}\n")

	if op.RequestContentType == "multipart/form-data" {
		buf.WriteString("\tmpBody, mpCT, err := req.encodeMultipart()\n")
		buf.WriteString("\tif err != nil {\n")
		fmt.Fprintf(buf, "\t\treturn %serr\n", ifZero(zero))
		buf.WriteString("\t}\n")
		buf.WriteString("\tr.RawBody = mpBody\n")
		buf.WriteString("\tr.RawContentType = mpCT\n")
	}

	fmt.Fprintf(buf, "\tbody, _, err := s.t.DoRaw(ctx, %s, %s, r)\n", httpMethod, pathExpr)
	buf.WriteString("\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\treturn %serr\n", ifZero(zero))
	buf.WriteString("\t}\n")
	switch {
	case op.ResponseType == "":
		buf.WriteString("\t_ = body\n\treturn nil\n")
	case op.ResponseType == "[]byte":
		buf.WriteString("\treturn body, nil\n")
	case strings.HasPrefix(op.ResponseType, "[]"):
		fmt.Fprintf(buf, "\tvar out %s\n", op.ResponseType)
		buf.WriteString("\tif err := json.Unmarshal(body, &out); err != nil {\n")
		fmt.Fprintf(buf, "\t\treturn %serr\n", ifZero(zero))
		buf.WriteString("\t}\n\treturn out, nil\n")
	default:
		fmt.Fprintf(buf, "\tvar out %s\n", op.ResponseType)
		buf.WriteString("\tif err := json.Unmarshal(body, &out); err != nil {\n")
		fmt.Fprintf(buf, "\t\treturn %serr\n", ifZero(zero))
		buf.WriteString("\t}\n\treturn &out, nil\n")
	}
}

// renderPathExpr produces the Go expression for the concrete request
// path, url.PathEscape-ing path parameters. Parameter names are looked
// up case- and separator-insensitively so a `{cardInvitationId}`
// template placeholder still resolves to a `card_invitation_id`
// parameter entry — Revolut's specs mix the two in the same operation.
func renderPathExpr(tmpl string, params []*PathParam) string {
	tmpl = strings.TrimPrefix(tmpl, "/")
	paramByName := map[string]*PathParam{}
	for _, p := range params {
		paramByName[canonicalParamKey(p.Name)] = p
	}

	var parts []string
	rest := tmpl
	for {
		i := strings.Index(rest, "{")
		if i < 0 {
			if rest != "" {
				parts = append(parts, fmt.Sprintf("%q", rest))
			}
			break
		}
		j := strings.Index(rest, "}")
		if j < 0 {
			parts = append(parts, fmt.Sprintf("%q", rest))
			break
		}
		literal := rest[:i]
		name := rest[i+1 : j]
		if literal != "" {
			parts = append(parts, fmt.Sprintf("%q", literal))
		}
		p, ok := paramByName[canonicalParamKey(name)]
		if !ok {
			parts = append(parts, fmt.Sprintf("%q", "{"+name+"}"))
		} else {
			parts = append(parts, fmt.Sprintf("url.PathEscape(%s)", p.GoName))
		}
		rest = rest[j+1:]
	}
	if len(parts) == 0 {
		return `""`
	}
	return strings.Join(parts, " + ")
}

// canonicalParamKey normalises a parameter name for case- and
// separator-insensitive lookup: lowercased, with underscores and
// hyphens stripped. "card_invitation_id" and "cardInvitationId" and
// "card-invitation-id" all collapse to "cardinvitationid".
func canonicalParamKey(s string) string {
	var b strings.Builder
	b.Grow(len(s))
	for _, r := range s {
		if r == '_' || r == '-' {
			continue
		}
		if r >= 'A' && r <= 'Z' {
			r += 'a' - 'A'
		}
		b.WriteRune(r)
	}
	return b.String()
}

func zeroValueForReturn(responseType string) string {
	switch {
	case responseType == "":
		return ""
	case strings.HasPrefix(responseType, "[]"):
		return "nil"
	default:
		return "nil"
	}
}

func ifZero(zero string) string {
	if zero == "" {
		return ""
	}
	return zero + ", "
}

func jsonTag(f *StructField) string {
	if f.Required {
		return f.JSONName
	}
	return f.JSONName + ",omitempty"
}

// writeStructFieldDoc emits godoc lines for a struct field, including
// the field's description and annotations for readOnly, writeOnly, and
// deprecated — which are expressed as schema flags in OpenAPI rather
// than inline prose.
func writeStructFieldDoc(buf *bytes.Buffer, f *StructField) {
	if f.Doc != "" {
		fmt.Fprintf(buf, "\t// %s\n", f.Doc)
	}
	switch {
	case f.ReadOnly && f.WriteOnly:
		buf.WriteString("\t//\n\t// Read/write-only flags conflict in the spec; treating as advisory.\n")
	case f.ReadOnly:
		buf.WriteString("\t//\n\t// Read-only: populated by the server; any value sent by the\n\t// client is ignored.\n")
	case f.WriteOnly:
		buf.WriteString("\t//\n\t// Write-only: accepted on requests but never echoed back in\n\t// responses.\n")
	}
	if f.Deprecated != "" {
		buf.WriteString("\t//\n")
		fmt.Fprintf(buf, "\t// Deprecated: %s\n", f.Deprecated)
	}
}

func writeDoc(buf *bytes.Buffer, doc, name string) {
	doc = strings.TrimSpace(doc)
	if doc == "" {
		return
	}
	lines := strings.Split(doc, "\n")
	for i, line := range lines {
		if i == 0 {
			fmt.Fprintf(buf, "// %s %s\n", name, lower1(line))
			continue
		}
		if line == "" {
			buf.WriteString("//\n")
			continue
		}
		fmt.Fprintf(buf, "// %s\n", line)
	}
}

func lower1(s string) string {
	if s == "" {
		return s
	}
	r := []rune(s)
	r[0] = lowerRune(r[0])
	return string(r)
}

func lowerRune(r rune) rune {
	if r >= 'A' && r <= 'Z' {
		return r + ('a' - 'A')
	}
	return r
}

func writeFormatted(path, source string) error {
	formatted, err := format.Source([]byte(source))
	if err != nil {
		// On format failure, still write unformatted source with .bad
		// suffix so we can inspect.
		bad := path + ".bad"
		_ = os.WriteFile(bad, []byte(source), 0o644)
		return fmt.Errorf("gofmt %s: %w (unformatted source written to %s)", path, err, bad)
	}
	return os.WriteFile(path, formatted, 0o644)
}
